const UNUSED_STATS: number[] = [84, 88, 192, 193, 202, 203, 205, 206, 207, 208, 209, 210, 211, 212, 268, 269, 270, 271, 272, 273, 274, 275, 276, 277, 278, 279, 280, 281, 282, 283, 284, 285, 286, 287, 288, 289, 290, 291, 292, 293, 294, 295, 296, 297, 298, 299, 300, 301, 302, 303, 304];

function build(): NumberedStatDef[] {
  const byId = new Map<number, NumberedStatDef>();
  for (const [id, label, category, redirectsTo] of labeled) {
    byId.set(id, { id, label, category, redirectsTo });
  }
  for (const [id, label] of MAP_STATS) {
    if (!byId.has(id)) byId.set(id, { id, label, category: "maps" });
  }
  for (const id of UNUSED_STATS) {
    if (!byId.has(id)) byId.set(id, { id, label: `unused${id}`, category: "unused" });
  }
  // Fill gaps with "unknown" so the validator knows the ID range 0..506 is valid tokens.
  for (let i = 0; i <= 506; i++) {
    if (!byId.has(i)) byId.set(i, { id: i, label: `STAT${i}`, category: "other" });
  }
  return Array.from(byId.values()).sort((a, b) => a.id - b.id);
}

export const NUMBERED_STATS: NumberedStatDef[] = build();

export const NUMBERED_STATS_BY_ID: Record<number, NumberedStatDef> = Object.fromEntries(
  NUMBERED_STATS.map((s) => [s.id, s])
);

